// Set sequence to start displaying
void setSequence(byte frameArray[][8], int framerateInt) {
  for(uint8_t x = 0; x < 4; x++) {
    for(uint8_t y = 0; y < 8; y++) {
      sequence[x][y] = frameArray[x][y];
    }
  }
  framerate = framerateInt;
  currentFrame = 0;
}

